// --- Завдання 3.1: Демонстрація базових типів даних ---

console.log("Приклади для кожного типу:");

// 1. Тип 'string' (рядок) - для текстових даних.
let bookTitle: string = "Майстер і Маргарита";
console.log("Приклад string (назва книги):", bookTitle);

// 2. Тип 'number' (число) - для цілих чисел та чисел з плаваючою комою.
let pageCount: number = 480;
console.log("Приклад number (кількість сторінок):", pageCount);

// 3. Тип 'boolean' (булевий) - для значень 'правда' (true) або 'неправда' (false).
let isAvailable: boolean = true;
console.log("Приклад boolean (чи є в наявності):", isAvailable);

// 4. Тип 'any' - для змінної, тип якої може змінюватися. [cite: 53]
// Його слід використовувати, коли тип даних наперед невідомий. [cite: 53]
let userData: any = "активний"; // Спочатку це рядок
console.log("Приклад 'any' (статус користувача як рядок):", userData);

// А тепер та ж сама змінна 'userData' буде містити число.
userData = 101; // Тепер це число
console.log("Приклад 'any' (ID користувача як число):", userData);


// --- Завдання 3.2: Демонстрація масивів ---

console.log("\nПриклади для масивів:");

// Масив, що містить тільки рядки: список покупок. [cite: 31]
let shoppingList: string[] = ["Молоко", "Хліб", "Яйця"];
console.log("Масив рядків (список покупок):", shoppingList);

// Масив, що містить тільки числа: результати тестів. [cite: 31]
let testScores: number[] = [95, 88, 100, 76];
console.log("Масив чисел (результати тестів):", testScores);