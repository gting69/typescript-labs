/**
 * Ця функція виводить вітання у консоль задану кількість разів.
 * @param name - Ім'я, яке буде у вітанні (тип: string).
 * @param count - Кількість повторень (тип: number). [cite_start]Має значення за замовчуванням 1. [cite: 35]
 * [cite_start]: void - означає, що функція нічого не повертає (лише виконує дію). [cite: 67]
 */
function greetWithNumber(name: string, count: number = 1): void {
    console.log(`\n--- Вітаємо ${name} ${count} раз(ів) ---`);
    for (let i = 0; i < count; i++) {
        console.log(`Привіт, ${name}! Це вітання номер ${i + 1}.`);
    }
}

// Викликаємо функцію, передаючи обидва параметри.
greetWithNumber("Студент", 3);

// Викликаємо функцію, передаючи тільки перший параметр.
// 'count' автоматично прийме значення за замовчуванням, тобто 1.
greetWithNumber("Гість");